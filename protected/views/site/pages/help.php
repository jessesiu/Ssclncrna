<p class=MsoListParagraph style='margin-left:18.0pt;text-align:justify;
text-justify:inter-ideograph'><span lang=EN-US style='font-family:"Helvetica","sans-serif";
color:#595959'>Long noncoding RNA (lncRNA) is a group of untranslated
transcripts. It had once been considered as junk transcripts, but recently
demonstrated to be a novel and versatile layer of molecular regulation. However
the function of majority of them still remained mysterious. A few
spermatogenesis-related lncRNA candidates were characterized in recent reports,
but the number is far away from completeness. Thanks to recent advance in
high-throughput transcriptomic platforms, including next generation RNA
sequencing (RNA-Seq), microarray and serial analysis of gene expression (SAGE),
a large number of lncRNAs have been discovered. It is a good opportunity now to
investigate this novel biomolecules </span><span
lang=EN-US style='font-family:"Helvetica","sans-serif";color:#595959'>(</span><span
lang=EN-US><a href="#_ENREF_8" title="Luk, 2014 #32821"><span style='font-family:
