annotated using high-throughput transcriptomic techniques, the current
annotations are built on studies using other model cell types, but not cell
types in spermatogenesis <i>per se</i>. Given lncRNAs are expressed in a rather
cell type-specific manner, over a thousand of novel lncRNAs not annotated by
current databases are likely to be spermatogenesis-specific and may be carrying
important biological functions. </span></p>

