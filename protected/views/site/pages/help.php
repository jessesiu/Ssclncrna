<p class=MsoListParagraph style='margin-left:18.0pt;text-align:justify;
text-justify:inter-ideograph'><span lang=EN-US style='font-family:"Helvetica","sans-serif";
color:#595959'>Spermatogenesis is a complex developmental process whereby
undifferentiated spermatogonia are differentiated into functional sperms
through a series of tightly regulated proliferation and differentiation programs.
As a result, identifying the critical genes controlling these processes will
not only dissect the basic molecular mechanisms, but also allow identification
of therapeutic targets for cancer, reproductive and regenerative medicine.</span></p>

<p class=MsoListParagraph style='margin-left:18.0pt;text-align:justify;
text-justify:inter-ideograph'><span lang=EN-US style='font-family:"Helvetica","sans-serif";
color:#595959'>Despite the biological importance of male germ cell development,
the underlying mechanisms of the stage-specific gene regulation and cellular
transition during spermatogenesis remain largely unknown. Previous genomic
studies on transcriptome profiling were limited to protein-coding genes. It is
now clear that protein-coding genes only represent small percentage of
transcriptome; most are non-coding transcripts. The functional importance of
this ¡§dark matter¡š in germ cell transcriptome remains largely elusive.</span></p>

